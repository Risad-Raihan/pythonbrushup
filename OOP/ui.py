#------------------------------------------------------------
# Helper: text wrapping for box display
#------------------------------------------------------------

def wrap_text(text: str, width: int):
    words = text.split()
    lines = []
    current = ""

    for word in words:
        if len(current) + len(word) + 1 <= width:
            current += (" " if current else "") + word
        else:
            lines.append(current)
            current = word

    if current:
        lines.append(current)

    return lines
